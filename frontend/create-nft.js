// VotingPowerNFT Contract Address
const VotingPowerNFTAddress = "0xb22d24BE5d608e5BD33d2b5D936A80b74d445CCd";

// Global variables for web3 and contract
let web3;
let accounts;
let votingPowerNFTContract;

// Function to initialize contract
async function initContract() {
    if (typeof window.ethereum !== 'undefined') {
        try {
            web3 = new Web3(window.ethereum);
            votingPowerNFTContract = new web3.eth.Contract(VotingPowerNFTABI, VotingPowerNFTAddress);
            return true;
        } catch (error) {
            console.error('Error initializing contract:', error);
            return false;
        }
    }
    return false;
}

// Function to mint NFT
async function mintNFT(recipientAddress) {
    try {
        // Get current gas price from the network
        const gasPrice = await web3.eth.getGasPrice();

        const result = await votingPowerNFTContract.methods
            .mintNFT(recipientAddress)
            .send({
                from: accounts[0],
                gas: 200000, // Set appropriate gas limit for minting
                gasPrice: gasPrice // Use current market gas price
            });

        return result;
    } catch (error) {
        console.error('Error minting NFT:', error);
        throw error;
    }
} 